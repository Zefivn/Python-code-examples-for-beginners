# Temel değişkenler örneği

# Sayısal değişkenler
tam_sayi = 10         # integer (tam sayı)
ondalik_sayi = 3.14   # float (ondalık sayı)

# Metin (string) değişkeni
isim = "Ali"          # string (metin)

# Boolean (mantıksal) değişken
dogru_mu = True       # boolean (True/False)

# Liste (list) değişkeni
sayilar = [1, 2, 3, 4, 5]

# Değişkenleri ekrana yazdırma
print("Tam sayi:", tam_sayi)
print("Ondalik sayi:", ondalik_sayi)
print("İsim:", isim)
print("Doğru mu?", dogru_mu)
print("Sayilar listesi:", sayilar)

# Değişkenler üzerinde basit işlemler
toplam = tam_sayi + ondalik_sayi
print("Tam sayi ve ondalık sayinin toplami:", toplam)


